package tpe.oo.abstrakt;
/**
 * This class implements the basic concepts of an geometric object.
 *
 * @author b.collins
 *
 */
public abstract class Figur {
    /**
     * the abstract method implements the
     * computation of the area of an geometric object.
     *
     * @return
     * @return returns the area of the object.
     */
    abstract double flaeche();

}
